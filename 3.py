numero_semana = int(input("Digite um numero de 1 a 7: "))

if numero_semana == 1:
    print(f"Domingo")

elif numero_semana == 2:
    print(f"Segunda")

elif numero_semana == 3:
    print(f"Terça")

elif numero_semana == 4:
    print(f"Quarta")

elif numero_semana == 5:
    print(f"Quinta")

elif numero_semana == 6:
    print(f"Sexta")

elif numero_semana == 7:
    print(f"Sábado")
else:
    print(f"Invalido")